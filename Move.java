
public class Move
	{

		String move;
		int score;
	public Move(String m, int s)
		{
		this.move = m;
		this.score = s;
		}
	public String getMove()
		{
		return move;
		}
	public void setMove(String move)
		{
		this.move = move;
		}
	public int getScore()
		{
		return score;
		}
	public void setScore(int score)
		{
		this.score = score;
		}

	}
